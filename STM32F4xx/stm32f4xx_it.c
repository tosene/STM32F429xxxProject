///////////////////////////////////////////////////////////////////////////////
//////函	   数： void USART1_IRQHandler(void)
//////功	   能： USART1的中断处理函数
//////输入参数: 
//////输出参数: 
//////说	   明： 
//////////////////////////////////////////////////////////////////////////////
void USART1_IRQHandler( void )
{
	if ( USART1_HandlerType->USARTxCOM!=NULL)
	{
		//---数据接收中断---接收寄存器不为空
		if ( LL_USART_IsActiveFlag_RXNE( USART1_HandlerType->USARTxCOM)&&LL_USART_IsEnabledIT_RXNE( USART1_HandlerType->USARTxCOM) )
		{
			//---接收处理函数
			USART1_HandlerType->USART_MSG_Task( USART1_HandlerType, LL_USART_ReceiveData8( USART1_HandlerType->USARTxCOM), USART_MSG_RXNE_Handler);
			//---清楚中断标志位
			LL_USART_ClearFlag_RXNE( USART1_HandlerType->USARTxCOM);
		}
		////---数据发送中断---数据发送寄存器空
		if ( LL_USART_IsActiveFlag_TXE( USART1_HandlerType->USARTxCOM)&&LL_USART_IsEnabledIT_TXE( USART1_HandlerType->USARTxCOM) )
		{
			//---发送处理函数
			USART1_HandlerType->USART_MSG_Task( USART1_HandlerType, 0, USART_MSG_TXE_Handler);
		}
		//---数据发送中断---数据发送完成
		if ( LL_USART_IsActiveFlag_TC( USART1_HandlerType->USARTxCOM)&&LL_USART_IsEnabledIT_TC( USART1_HandlerType->USARTxCOM) )
		{
			//---数据发送完成处理函数
			USART1_HandlerType->USART_MSG_Task( USART1_HandlerType, 0, USART_MSG_TC_Handler);
			//---清楚中断标志位
			LL_USART_ClearFlag_TC( USART1_HandlerType->USARTxCOM);
		}
		/*
		//---用于DMA传输使用
		if ( LL_USART_IsActiveFlag_IDLE( USART1 )&&LL_USART_IsEnabledIT_IDLE( USART1 ) )
		{
			LL_USART_ReceiveData8( USART1 );
			//---清楚中断标志位
			LL_USART_ClearFlag_IDLE(pUSART1->USARTxCOM);
		}
		*/
	}
}

