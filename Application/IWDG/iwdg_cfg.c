void IWDG_Init(void)
{
	//---使能预分频寄存器和重装载寄存器可写（取消寄存器的写保护）
	LL_IWDG_EnableWriteAccess( IWDG );
	//---设置预分频器值
	LL_IWDG_SetPrescaler( IWDG, LL_IWDG_PRESCALER_4 );
	//---设置重装载寄存器值
	LL_IWDG_SetReloadCounter( IWDG, 1023 );
	//---等待独立看门狗准备完成
	while( LL_IWDG_IsReady( IWDG ) != 1 )
	{
	}
	//---使能看门狗
	LL_IWDG_Enable( IWDG );
	//---复位看门狗
	LL_IWDG_ReloadCounter( IWDG );
}

